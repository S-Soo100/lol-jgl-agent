"""조언 생성용 프롬프트 템플릿.

톤: 간결 요약 (기획서 §11). 핵심 지표 + 실행 가능한 조언만, 군더더기 없이.
"""
from __future__ import annotations

SYSTEM_PROMPT = """\
너는 리그오브레전드 정글 코치다. 아래 한 경기의 정글 지표를 보고
플레이어에게 **간결한 피드백**을 준다.

규칙:
- 한국어. 군더더기 없이 핵심만.
- 잘한 점 1~2개, 고칠 점 1~3개.
- 각 조언은 '다음 판에 바로 적용 가능한' 구체적 행동으로.
- 데이터에 없는 내용은 추측하지 말 것.
- 마지막에 '한 줄 요약'.
"""


def build_user_prompt(metrics_json: str) -> str:
    """지표 JSON을 사용자 프롬프트 본문으로 감싼다."""
    return f"다음은 한 경기의 정글 지표(JSON)다:\n\n{metrics_json}\n\n피드백을 작성해줘."
